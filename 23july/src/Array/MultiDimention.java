package Array;

public class MultiDimention {
	public static void main(String[] args) {
		
	
       int a[][]= {{1,7,7},{1,8,8}};
       for(int i=0;i<a.length;i++) 
       {
    	   for(int j=0;j<a.length;j++) {
    		   System.out.print(" "+ a[i][j]);
    	   }
    	   System.out.println();
       }
       
}
}