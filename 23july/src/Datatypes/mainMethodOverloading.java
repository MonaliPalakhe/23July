package Datatypes;

public class mainMethodOverloading {
    
     public static void main(int a) {
    	 System.out.println(a);
     }
     public static void main(char m) {
    	 System.out.println(m);
     }
     public static void main(String[] args) {
    	 main(23);
    	 main('X');
     }
	
	
}
