package Collections;

import java.util.ArrayList;



public class ArrayListInt {
	public static void main(String[] args) {
		ArrayList <Integer> set=new ArrayList <Integer> ();
	  
		  set.add(45);
		  set.add(56);
		  set.add(67);
		  set.add(89);
		  
		  //Advanced for loop
		  for(int t:set)
		  {
			System.out.println(t);  
		  }

	}
}
